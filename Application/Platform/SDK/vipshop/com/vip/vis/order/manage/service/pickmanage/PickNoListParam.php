class PickNoListParam {
	
	static $_TSPEC;
	public $source = null;
	public $pickNoList = null;
	
	public function __construct($vals=null){
		
		if (!isset(self::$_TSPEC)){
			
			self::$_TSPEC = array(
			1 => array(
			'var' => 'source'
			),
			2 => array(
			'var' => 'pickNoList'
			),
			
			);
			
		}
		
		if (is_array($vals)){
			
			
			if (isset($vals['source'])){
				
				$this->source = $vals['source'];
			}
			
			
			if (isset($vals['pickNoList'])){
				
				$this->pickNoList = $vals['pickNoList'];
			}
			
			
		}
		
	}
	
	
	public function getName(){
		
		return 'PickNoListParam';
	}
	
	public function read($input){
		
		$input->readStructBegin();
		while(true){
			
			$schemeField = $input->readFieldBegin();
			if ($schemeField == null) break;
			$needSkip = true;
			
			
			if ("source" == $schemeField){
				
				$needSkip = false;
				$input->readString($this->source);
				
			}
			
			
			
			
			if ("pickNoList" == $schemeField){
				
				$needSkip = false;
				
				$this->pickNoList = array();
				$_size0 = 0;
				$input->readListBegin();
				while(true){
					
					try{
						
						$elem0 = null;
						$input->readString($elem0);
						
						$this->pickNoList[$_size0++] = $elem0;
					}
					catch(\Exception $e){
						
